from __future__ import print_function
from __future__ import division
from __future__ import absolute_import
import numpy as np

def RMSD(_labels, _predictions):
    _labels = np.array(_labels)
    _predictions = np.array(_predictions)

    diff = _labels - _predictions
    squareDiff = diff*diff
    meanDiff = np.mean(squareDiff)
    sqrtMeanDiff = np.sqrt(meanDiff)

    return sqrtMeanDiff

